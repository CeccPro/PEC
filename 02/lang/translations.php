/**
 * Función auxiliar para obtener traducción
 * @param string $key
 * @param string $lang
 * @param array $replacements
 * @return string
 */
function translate($key, $lang = 'es', $replacements = []) {
    global $translations;
    
    $text = $translations[$lang][$key] ?? $translations['es'][$key] ?? $key;
    
    // Reemplazo de variables en el texto
    foreach ($replacements as $placeholder => $value) {
        $text = str_replace('{' . $placeholder . '}', $value, $text);
    }
    
    return $text;
}

/**
 * Función para verificar si una clave de traducción existe
 * @param string $key
 * @param string $lang
 * @return bool
 */
function translationExists($key, $lang = 'es') {
    global $translations;
    return isset($translations[$lang][$key]);
}

/**
 * Función para obtener todas las claves faltantes en un idioma
 * @param string $lang
 * @return array
 */
function getMissingTranslations($lang) {
    global $translations;
    
    $base_keys = array_keys($translations['es']);
    $target_keys = array_keys($translations[$lang] ?? []);
    
    return array_diff($base_keys, $target_keys);
}
